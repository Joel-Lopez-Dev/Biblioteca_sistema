    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
        autor=Autor.getText();
        libro_editar.Autor=autor;
        libros.remove(indice);
        libros.add(libro_editar);
        try {   
                File archivo= new File("src\\main\\java\\Libros\\Libros.obj");
                archivo.delete();
                FileOutputStream file= new FileOutputStream("src\\main\\java\\Libros\\Libros.obj");
                ObjectOutputStream escribir=new ObjectOutputStream(file);

                for(Libro lib : libros) // For each.
                {
                    escribir.writeObject(lib);
                    escribir.reset();
                }
                    
                
                escribir.close();
            } catch (FileNotFoundException ex) {
                JOptionPane.showMessageDialog(null,"Error\nNo se ha encontrado el archivo papuu");
            } catch (IOException ex) {
                JOptionPane.showMessageDialog(null,"Error\nNo se ha encontrado el archivo papu");
            }
        JOptionPane.showMessageDialog(null,"Se ha modificado el libro");
    }//GEN-LAST:event_jButton8ActionPerformed

