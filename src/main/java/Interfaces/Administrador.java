    private void editarsinActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editarsinActionPerformed
        Es_numero comprobar = new Es_numero();
        
        autor=autorsin.getText();
        nombre=nombresin.getText();
        genero=generin.getText();
        paginas=numerin.getText();
        cantidad=cantidadsin.getText();
        año=añin.getText();
        comprobar.run();
        /*
        for(int i=0;i<paginas.length();i++){
            c=paginas.charAt(i);
            if(comprobar.entro==true){
                break;
            }
        }
        for(int i=0;i<año.length();i++){
            c2=año.charAt(i);
            if(comprobar.entro==true){
                break;
            }
        }
        for(int i=0;i<cantidad.length();i++){
            c3=cantidad.charAt(i);
            if(comprobar.entro==true){
                break;
            }
        }
        */
        int añosin,paginin,cantidadin;
        if(Es_numero.entro==false){
            añosin=Integer.parseInt(año);
            paginin=Integer.parseInt(paginas);
            cantidadin=Integer.parseInt(cantidad);
            Libro libro= new Libro(nombre,autor,genero,añosin,paginin,cantidadin);
            libros.add(libro);
            try {
                File archivin = new File("src\\main\\java\\Libros\\Libros.obj");
                archivin.delete();
                FileOutputStream file= new FileOutputStream("src\\main\\java\\Libros\\Libros.obj",true);
                ObjectOutputStream escribir=new ObjectOutputStream(file);

                for(Libro lib : libros) // For each.
                {
                    escribir.writeObject(lib);
                    escribir.reset();
                }
                    
                contador+=1;
                escribir.close();
            } catch (FileNotFoundException ex) {
                JOptionPane.showMessageDialog(null,"Error\nNo se ha encontrado el archivo papuu");
            } catch (IOException ex) {
                JOptionPane.showMessageDialog(null,"Error\nNo se ha encontrado el archivo papu");
            }
            JOptionPane.showMessageDialog(null,"Se ha agregado el libro");
            
        }else{
            JOptionPane.showMessageDialog(null,"Ingresa un numero valido");
        }
        
    }//GEN-LAST:event_editarsinActionPerformed

