    private void BuscadorKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_BuscadorKeyReleased
        Libros.setText("");
        String mostrar = "";
        for(int i=0;i<libros.size();i++){
            if(libros.get(i).buscador(Buscador.getText())){
                
                encontrado=true;
                indice=i;
                libro_editar=libros.get(i);
                mostrar += libros.get(i).toString()+"\n\n";
                
            }
            
            
        }
        
        Libros.setText(mostrar);
    }//GEN-LAST:event_BuscadorKeyReleased

